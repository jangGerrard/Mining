package kr.ac.kw.mining.domain;

public class Event {
	private String start;
	private String end;
	private String title;
	private String description;
	private String link;
	private String image;
	
	public Event(){
		
	}
	
	public Event(String start, String end, String title, String description,
			String link, String image) {
		super();
		this.start = start;
		this.end = end;
		this.title = title;
		this.description = description;
		this.link = link;
		this.image = image;
	}
	
	public void setEvent(String start, String end, String title, String description,
			String link, String image){
		this.start = start;
		this.end = end;
		this.title = title;
		this.description = description;
		this.link = link;
		this.image = image;
	}
	
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
}
