package kr.ac.kw.mining.domain;

public class ImageLinks {
	
	private int ilFrom;
	private String ilTo;
	private String ilUrl;
	
	public int getIlFrom() {
		return ilFrom;
	}
	public void setIlFrom(int ilFrom) {
		this.ilFrom = ilFrom;
	}
	public String getIlTo() {
		return ilTo;
	}
	public void setIlTo(String ilTo) {
		this.ilTo = ilTo;
	}
	public String getIlUrl() {
		return ilUrl;
	}
	public void setIlUrl(String ilUrl) {
		this.ilUrl = ilUrl;
	}
	
}
