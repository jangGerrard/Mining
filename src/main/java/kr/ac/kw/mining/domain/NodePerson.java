package kr.ac.kw.mining.domain;

public class NodePerson {
	private int personPageId;
	private String personPageTitle;
	private int year ;
	private int month ;
	private int date;
	public int getPersonPageId() {
		return personPageId;
	}
	public void setPersonPageId(int personPageId) {
		this.personPageId = personPageId;
	}
	public String getPersonPageTitle() {
		return personPageTitle;
	}
	public void setPersonPageTitle(String personPageTitle) {
		this.personPageTitle = personPageTitle;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	}
	
	
}
