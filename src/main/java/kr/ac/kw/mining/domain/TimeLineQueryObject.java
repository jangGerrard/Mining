package kr.ac.kw.mining.domain;

public class TimeLineQueryObject {
	private int yearSelection;
	private String personString;
	
	
	public int getYearSelection() {
		return yearSelection;
	}
	public void setYearSelection(int yearSelection) {
		this.yearSelection = yearSelection;
	}
	public String getPersonString() {
		return personString;
	}
	public void setPersonString(String personString) {
		this.personString = personString;
	}
}
